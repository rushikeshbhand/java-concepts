class MultiplyFloatingNumbers {               //class
    public static void main(String[] args) {       //mainfunction
        float number1 = 2.5f;
        float number2 = 1.5f;

        float result = number1 * number2;     //multiplication of float numbers

        System.out.println("The product of " + number1 + " and " + number2 + " is: " + result);
    }
}
